package variablesAndDataTypes;

/*
Exercise 3: Write a Java program to declare two integer variables,
one float variable, and one string variable and assign 10, 12.5, 
and "Java programming" to them respectively. Then display their values on the screen
*/

public class Exercise3 {
	
	public static void main(String[] args) {
		
		int var1 = 10;
		float var2 = 12.5f;
		String var3 = "Java progamming";
		
		System.out.println(var1);
		System.out.println(var2);
		System.out.println(var3);
	}

}
